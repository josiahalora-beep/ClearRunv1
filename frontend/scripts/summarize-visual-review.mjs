import fs from "node:fs";
import path from "node:path";

const resultsDir = path.resolve("test-results");
const axeDir = path.join(resultsDir, "axe");
const lighthouseDir = path.join(resultsDir, "lighthouse");
const summaryPath = path.join(resultsDir, "visual-review-summary.md");

function exists(targetPath) {
  return fs.existsSync(targetPath);
}

function readJson(filePath) {
  try {
    return JSON.parse(fs.readFileSync(filePath, "utf8"));
  } catch (error) {
    return { error: error.message };
  }
}

function listFilesRecursive(dir, matcher) {
  if (!exists(dir)) return [];
  const output = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      output.push(...listFilesRecursive(fullPath, matcher));
    } else if (!matcher || matcher(fullPath)) {
      output.push(fullPath);
    }
  }
  return output;
}

function summarizeAxe() {
  const files = listFilesRecursive(axeDir, (filePath) => filePath.endsWith(".json"));
  if (!files.length) {
    return {
      rows: [],
      totalBlocking: 0,
      markdown: ["## Axe Accessibility", "", "No axe JSON files were found."].join("\n"),
    };
  }

  const rows = files.map((filePath) => {
    const json = readJson(filePath);
    const relative = path.relative(axeDir, filePath);
    const violations = Array.isArray(json.violations) ? json.violations : [];
    const serious = violations.filter((item) => item.impact === "serious").length;
    const critical = violations.filter((item) => item.impact === "critical").length;
    const moderate = violations.filter((item) => item.impact === "moderate").length;
    const minor = violations.filter((item) => item.impact === "minor").length;
    const topIssues = violations
      .filter((item) => ["critical", "serious"].includes(item.impact))
      .slice(0, 4)
      .map((item) => `${item.id} (${item.impact})`)
      .join(", ");

    return {
      file: relative,
      critical,
      serious,
      moderate,
      minor,
      blocking: critical + serious,
      topIssues: topIssues || "None",
    };
  });

  const totalBlocking = rows.reduce((sum, row) => sum + row.blocking, 0);
  const markdown = [
    "## Axe Accessibility",
    "",
    `Blocking serious/critical violations: ${totalBlocking}`,
    "",
    "| Report | Critical | Serious | Moderate | Minor | Top blocking issues |",
    "|---|---:|---:|---:|---:|---|",
    ...rows.map((row) => `| ${row.file} | ${row.critical} | ${row.serious} | ${row.moderate} | ${row.minor} | ${row.topIssues} |`),
  ].join("\n");

  return { rows, totalBlocking, markdown };
}

function summarizeLighthouse() {
  const summaryFile = path.join(lighthouseDir, "summary.md");
  if (!exists(summaryFile)) {
    return ["## Lighthouse", "", "No Lighthouse summary was found."].join("\n");
  }

  return ["## Lighthouse", "", fs.readFileSync(summaryFile, "utf8")].join("\n");
}

fs.mkdirSync(resultsDir, { recursive: true });

const axe = summarizeAxe();
const lighthouse = summarizeLighthouse();

const markdown = [
  "# ClearRun Frontend Visual Review Summary",
  "",
  "This report is generated from the production build visual-review workflow.",
  "",
  "## What this checks",
  "",
  "- React production build can compile and serve.",
  "- Key ClearRun pages render at mobile, tablet, and desktop widths.",
  "- Full-page screenshots are captured for visual review.",
  "- Axe checks identify accessibility blockers.",
  "- Lighthouse reports performance, accessibility, best-practices, and SEO.",
  "",
  axe.markdown,
  "",
  lighthouse,
  "",
  "## Artifact folders",
  "",
  "- `test-results/screenshots/`",
  "- `test-results/axe/`",
  "- `test-results/playwright-report/`",
  "- `test-results/lighthouse/`",
  "",
  "Generated by `npm run visual:summary`.",
].join("\n");

fs.writeFileSync(summaryPath, markdown);
