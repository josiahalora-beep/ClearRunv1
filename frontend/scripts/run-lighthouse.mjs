import fs from "node:fs";
import path from "node:path";
import process from "node:process";
import lighthouseModule from "lighthouse";
import { launch as launchChrome } from "chrome-launcher";

const lighthouse = lighthouseModule.default || lighthouseModule;

const routes = [
  { name: "home", path: "/" },
  { name: "closeout-check", path: "/closeout-check" },
  { name: "proof-snapshot", path: "/proof-snapshot" },
  { name: "dashboard", path: "/dashboard" },
  { name: "route-review", path: "/route-review/warner-robins-route-b" },
  { name: "report-route-issue", path: "/report-issue/warner-robins-route-b" },
  { name: "route-issue-detail", path: "/issues/EX-2101" },
  { name: "proof-list", path: "/proof" },
  { name: "proof-detail", path: "/proof/PP-10234" },
];

const baseURL = (process.env.PLAYWRIGHT_BASE_URL || "http://127.0.0.1:4173").replace(/\/$/, "");
const outputDir = path.resolve("test-results", "lighthouse");
fs.mkdirSync(outputDir, { recursive: true });

function routeURL(routePath) {
  return `${baseURL}${routePath === "/" ? "" : routePath}`;
}

function scoreValue(score) {
  return typeof score === "number" ? Math.round(score * 100) : null;
}

function safeCell(value) {
  return value ?? "n/a";
}

const summary = [];
let hadFailure = false;
let chrome;

try {
  chrome = await launchChrome({
    chromeFlags: [
      "--headless=new",
      "--no-sandbox",
      "--disable-gpu",
      "--disable-dev-shm-usage",
    ],
  });

  for (const route of routes) {
    const url = routeURL(route.path);

    try {
      const result = await lighthouse(url, {
        port: chrome.port,
        output: ["json", "html"],
        logLevel: "error",
        onlyCategories: ["performance", "accessibility", "best-practices", "seo"],
      });

      if (!result || !result.lhr) {
        throw new Error("Lighthouse returned no result object.");
      }

      const [jsonReport, htmlReport] = Array.isArray(result.report)
        ? result.report
        : [result.report || JSON.stringify(result.lhr, null, 2), ""];

      fs.writeFileSync(path.join(outputDir, `${route.name}.json`), jsonReport || JSON.stringify(result.lhr, null, 2));
      fs.writeFileSync(path.join(outputDir, `${route.name}.html`), htmlReport || "");

      const categories = result.lhr.categories || {};
      const row = {
        route: route.path,
        performance: scoreValue(categories.performance?.score),
        accessibility: scoreValue(categories.accessibility?.score),
        bestPractices: scoreValue(categories["best-practices"]?.score),
        seo: scoreValue(categories.seo?.score),
        status: "ok",
        error: "",
      };
      summary.push(row);

      const blocking = [row.accessibility, row.bestPractices, row.seo].some((score) => score !== null && score < 80);
      if (blocking) hadFailure = true;
    } catch (error) {
      hadFailure = true;
      const message = error instanceof Error ? error.message : String(error);
      fs.writeFileSync(
        path.join(outputDir, `${route.name}-error.json`),
        JSON.stringify({ route: route.path, url, error: message }, null, 2)
      );
      summary.push({
        route: route.path,
        performance: null,
        accessibility: null,
        bestPractices: null,
        seo: null,
        status: "error",
        error: message.replace(/\|/g, "\\|"),
      });
      console.error(`Lighthouse failed for ${route.path}: ${message}`);
    }
  }
} catch (error) {
  hadFailure = true;
  const message = error instanceof Error ? error.message : String(error);
  fs.writeFileSync(path.join(outputDir, "runner-error.json"), JSON.stringify({ error: message }, null, 2));
  summary.push({
    route: "runner",
    performance: null,
    accessibility: null,
    bestPractices: null,
    seo: null,
    status: "error",
    error: message.replace(/\|/g, "\\|"),
  });
  console.error(`Lighthouse runner failed: ${message}`);
} finally {
  if (chrome) {
    await chrome.kill();
  }
}

const markdown = [
  "# ClearRun Lighthouse Review",
  "",
  `Base URL: ${baseURL}`,
  "",
  "| Route | Status | Performance | Accessibility | Best Practices | SEO | Error |",
  "|---|---|---:|---:|---:|---:|---|",
  ...summary.map((row) => `| ${row.route} | ${row.status} | ${safeCell(row.performance)} | ${safeCell(row.accessibility)} | ${safeCell(row.bestPractices)} | ${safeCell(row.seo)} | ${row.error || ""} |`),
  "",
  "Generated by `npm run lighthouse:review`.",
].join("\n");

fs.writeFileSync(path.join(outputDir, "summary.md"), markdown);
console.log(markdown);

if (hadFailure && process.env.LIGHTHOUSE_STRICT === "1") {
  console.error("One or more Lighthouse routes failed or non-performance scores fell below 80.");
  process.exit(1);
}
