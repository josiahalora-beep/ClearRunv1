function createReadyStop(route, index, disposalStatus) {
  return {
    id: `${route.id}-ready-${index + 1}`,
    routeId: route.id,
    exceptionId: null,
    serviceOutcome: "Completed",
    closeoutState: "Ready to Close",
    disposalStatus,
    recordedDelayMinutes: 0,
    potentiallyUnbillable: false,
  };
}

export const routeStops = routeDefinitions.flatMap((route) => {
  const readyStops = route.readyDisposalStatuses.map((status, index) => createReadyStop(route, index, status));
  const exceptionStops = routeIntelligenceExceptions
    .filter((exception) => exception.routeId === route.id)
    .map((exception) => ({
      id: exception.stopId,
      routeId: route.id,
      exceptionId: exception.id,
      serviceOutcome: exception.serviceOutcome,
      closeoutState: exception.closeoutState,
      disposalStatus: exception.disposalStatus,
      recordedDelayMinutes: exception.recordedDelayMinutes,
      potentiallyUnbillable: exception.potentiallyUnbillable,
    }));

  return [...readyStops, ...exceptionStops];
});

export function getPriorityRank(priority) {
  const rank = PRIORITY_ORDER.indexOf(priority);
  return rank === -1 ? PRIORITY_ORDER.length : rank;
}

export function meetsRecurringThreshold(sample) {
  if (!sample || sample.denominator <= 0) return false;
  return sample.occurrences >= 3 && sample.occurrences / sample.denominator >= 0.2;
}

export function getRecurringPattern(routeId) {
  const route = routeDefinitions.find((item) => item.id === routeId);
  if (!route) return null;

  const qualifying = route.patternSamples
    .filter(meetsRecurringThreshold)
    .map((sample) => ({
      ...sample,
      percentage: Math.round((sample.occurrences / sample.denominator) * 100),
    }))
    .sort((a, b) => b.percentage - a.percentage || b.occurrences - a.occurrences);

  return qualifying[0] || null;
}

export function getRouteExceptions(routeId, filters = {}) {
  return routeIntelligenceExceptions
    .filter((exception) => exception.routeId === routeId)
    .filter((exception) => !filters.lane || filters.lane === "all" || exception.lane === filters.lane)
    .filter((exception) => !filters.disposalStatus || exception.disposalStatus === filters.disposalStatus)
    .sort((a, b) => getPriorityRank(a.priority) - getPriorityRank(b.priority) || b.ageHours - a.ageHours);
}

export function getDisposalMatrix(routeId) {
  const stops = routeStops.filter((stop) => stop.routeId === routeId);
  return DISPOSAL_STATUS_ORDER.map((status) => ({
    status,
    count: stops.filter((stop) => stop.disposalStatus === status).length,
  })).filter((row) => row.count > 0);
}

export function getRouteSummary(routeId) {
  const route = routeDefinitions.find((item) => item.id === routeId);
  if (!route) return null;

  const stops = routeStops.filter((stop) => stop.routeId === routeId);
  const exceptions = getRouteExceptions(routeId);
  const primaryException = exceptions[0] || null;
  const closeoutCounts = Object.fromEntries(
    CLOSEOUT_STATE_ORDER.map((state) => [state, stops.filter((stop) => stop.closeoutState === state).length])
  );
  const recordsNotReady = stops.filter((stop) => stop.closeoutState !== "Ready to Close").length;
  const recordedDelayMinutes = stops.reduce((total, stop) => total + stop.recordedDelayMinutes, 0);
  const potentiallyUnbillableStops = stops.filter((stop) => stop.potentiallyUnbillable).length;
  const activeExceptions = exceptions.filter((exception) => exception.lane === "active");
  const closeoutExceptions = exceptions.filter((exception) => exception.lane === "closeout");

  return {
    ...route,
    stops,
    exceptions,
    activeExceptions,
    closeoutExceptions,
    scheduledStops: stops.length,
    completedWithoutException: stops.filter((stop) => !stop.exceptionId).length,
    completedWithException: exceptions.filter((exception) => exception.serviceOutcome.includes("Completed")).length,
    serviceNotCompleted: exceptions.filter((exception) => exception.serviceOutcome === "Not completed").length,
    stopsRequiringFollowUp: exceptions.filter((exception) => exception.lane === "active" && exception.resolutionStatus !== "Resolved").length,
    recordsNotReady,
    recordedDelayMinutes,
    potentiallyUnbillableStops,
    closeoutCounts,
    primaryException,
    recurringPattern: getRecurringPattern(routeId),
    disposalMatrix: getDisposalMatrix(routeId),
    currentRouteStatus:
      primaryException?.priority === "Resolve Now" || potentiallyUnbillableStops > 0
        ? "At Risk"
        : recordsNotReady > 0
          ? "Needs Attention"
          : "Ready to Close",
  };
}

